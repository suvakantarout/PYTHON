# List_methods:
L1 = [1,8,7,2,21,18,4]
print(L1)

# sort method:
L1.sort()
print(L1)

# Append method:
L1.append(50)
print(L1)                  # add 50 at the end of the list

# Insert method:
L1 = [1,8,7,2,21,18,4]
L1.insert(3,9)
print(L1)                  # this will add 9 at 3 index

# Reversr method:
L1 = [1,8,7,2,21,18,4]
L1.reverse()
print(L1)
L1 = [1,8,7,2,21,18,4]     # print the reverse of the list
L1.pop(2)
print(L1)                  # it will delete the element at index 2
L1 = [1,8,7,2,21,18,4]
L1.remove(21)
print(L1)                  # it will remove 21 from the list