# String_functions:

story = ("once upon a time there was a king in a kingdom")

# len functions:
print(len(story))

# string.endswith:
print(story.endswith ("kingdom"))
print(story.endswith("upon"))

# string.count:
print(story.count("o"))
print(story.count("a"))
print(story.count("time"))
print(story.count("king"))

# string.capatilize:
print(story.capitalize())

# string.find:
print(story.find("king"))
print(story.find("kingom"))
print(story.find("once"))

# string.replace:
print(story.replace("kingdom" , "empror"))
